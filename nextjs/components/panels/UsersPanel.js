import { useCallback, useEffect, useState } from 'react';

const VISIBLE_COLUMNS = ['id', 'user_id', 'name', 'role', 'created_at'];

export default function UsersPanel({ auth, apiCall }) {
  const [users, setUsers] = useState([]);
  const [loading, setLoading] = useState(false);

  const [newId, setNewId] = useState('');
  const [newName, setNewName] = useState('');
  const [newPw, setNewPw] = useState('');
  const [newRole, setNewRole] = useState('사용자');
  const [msg, setMsg] = useState('');

  // M50: loadUsers를 useCallback으로 안정화
  const loadUsers = useCallback(async () => {
    setLoading(true);
    const res = await apiCall({ endpoint: '/api/users', auth, timeoutMs: 30000 });
    setLoading(false);
    if (res?.status === 'success' && Array.isArray(res.data)) setUsers(res.data);
    else setUsers([]);
  }, [apiCall, auth]);

  useEffect(() => {
    loadUsers();
  }, [loadUsers]);

  async function addUser() {
    setMsg('');
    if (!newId || !newName || !newPw) return;

    const res = await apiCall({
      endpoint: '/api/users',
      method: 'POST',
      auth,
      data: { user_id: newId, name: newName, password: newPw, role: newRole },
      timeoutMs: 30000,
    });

    if (res?.status === 'success') {
      setMsg(`${newName} 추가됨`);
      setNewId('');
      setNewName('');
      setNewPw('');
      setNewRole('사용자');
      await loadUsers();
    } else {
      setMsg('추가 실패');
    }
  }

  return (
    <div>
      <div className="flex items-end justify-between gap-3 mb-3">
        <div>
          <h2 className="text-lg md:text-xl font-semibold text-cafe24-brown">셀러 관리</h2>
          <p className="text-sm text-cafe24-brown/60">계정 목록 확인 및 추가</p>
        </div>
      </div>

      <div className="card mb-4">
        <div className="card-header">사용자 목록</div>
        {loading ? <div className="text-sm text-cafe24-brown/60">로딩 중...</div> : null}
        <div className="overflow-auto">
          <table className="table">
            <thead>
              <tr>
                {/* L27: 빈 배열일 때도 기본 컬럼 헤더 표시 */}
                {(users?.length ? VISIBLE_COLUMNS.filter((k) => k in users[0]) : VISIBLE_COLUMNS).map((k) => (
                  <th key={k}>{k}</th>
                ))}
              </tr>
            </thead>
            <tbody>
              {users?.length ? users.map((u, idx) => (
                <tr key={idx}>
                  {VISIBLE_COLUMNS.filter((k) => k in users[0]).map((k) => (
                    <td key={k}>{String(u[k])}</td>
                  ))}
                </tr>
              )) : (
                <tr><td colSpan={VISIBLE_COLUMNS.length} className="py-3 text-cafe24-brown/70 text-center">데이터 없음</td></tr>
              )}
            </tbody>
          </table>
        </div>
      </div>

      <div className="card">
        <div className="card-header">사용자 추가</div>

        <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-4 gap-3">
          <div>
            <label className="text-sm text-cafe24-brown/70">아이디</label>
            <input className="input mt-1" value={newId} onChange={(e) => setNewId(e.target.value)} />
          </div>
          <div>
            <label className="text-sm text-cafe24-brown/70">이름</label>
            <input className="input mt-1" value={newName} onChange={(e) => setNewName(e.target.value)} />
          </div>
          <div>
            <label className="text-sm text-cafe24-brown/70">비밀번호</label>
            <input className="input mt-1" type="password" value={newPw} onChange={(e) => setNewPw(e.target.value)} />
          </div>
          <div>
            <label className="text-sm text-cafe24-brown/70">권한</label>
            <select className="input mt-1" value={newRole} onChange={(e) => setNewRole(e.target.value)}>
              <option value="사용자">사용자</option>
              <option value="관리자">관리자</option>
            </select>
          </div>
        </div>

        <button className="btn w-full mt-3" onClick={addUser} disabled={!newId || !newName || !newPw}>
          추가
        </button>

        {msg ? (
          <div className="mt-3 rounded-xl border border-cafe24-orange/20 bg-cafe24-beige px-3 py-2 text-sm text-cafe24-brown/80">
            {msg}
          </div>
        ) : null}
      </div>
    </div>
  );
}
